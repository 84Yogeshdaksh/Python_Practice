# Program 5: Variable Naming Conventions

# Snake case - commonly used in Python for variable and function names
student_name = "John Doe"
student_age = 20

# Camel case - often seen in languages like JavaScript
studentName = "Jane Smith"
studentAge = 22

# Pascal case - typically used in class names
StudentName = "Michael Johnson"
StudentAge = 23

# Printing variables to show different naming conventions
print("Using snake_case:", student_name, student_age)
print("Using camelCase:", studentName, studentAge)
print("Using PascalCase:", StudentName, StudentAge)
