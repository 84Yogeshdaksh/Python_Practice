# Program 1: Dynamic Typing Demonstration

# Assign an integer value
variable = 10
print("Value:", variable, "| Type:", type(variable))

# Reassign a float value
variable = 10.5
print("Value:", variable, "| Type:", type(variable))

# Reassign a string value
variable = "Hello, Python!"
print("Value:", variable, "| Type:", type(variable))

# Reassign a list
variable = [1, 2, 3, 4]
print("Value:", variable, "| Type:", type(variable))
